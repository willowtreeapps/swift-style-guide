import Foundation

extension Array {
    public func mergeSort() -> Array<Element> {
        return self
    }
}

public func mergeSort(array: Array<Int>) -> Array<Int> {
    return array
}

public func zip(a: Int, b: Int) -> (Int, Int) {
    return (a, b)
}

public func max(a: Int, b: Int, c: Int) -> Int {
    return max(a, b, c)
}
